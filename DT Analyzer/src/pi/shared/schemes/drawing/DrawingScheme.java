package pi.shared.schemes.drawing;

import java.awt.Color;

public class DrawingScheme
{

	private Color borderColor;
	private Color backgroundColor;
	private Color drawingColor;
	private Color angleColor;
	private Color linearizedColor;
	private Color figureColor;
	private Color allColor;
	private Color selectionColor;
	private Color labelColor;

	public Color getBorderColor()
	{
		return borderColor;
	}

	public void setBorderColor(Color borderColor)
	{
		this.borderColor = borderColor;
	}

	public Color getBackgroundColor()
	{
		return backgroundColor;
	}

	public void setBackgroundColor(Color backgroundColor)
	{
		this.backgroundColor = backgroundColor;
	}

	public Color getDrawingColor()
	{
		return drawingColor;
	}

	public void setDrawingColor(Color drawingColor)
	{
		this.drawingColor = drawingColor;
	}

	public Color getAngleColor()
	{
		return angleColor;
	}

	public void setAngleColor(Color angleColor)
	{
		this.angleColor = angleColor;
	}

	public Color getLinearizedColor()
	{
		return linearizedColor;
	}

	public void setLinearizedColor(Color linearizedColor)
	{
		this.linearizedColor = linearizedColor;
	}

	public Color getFigureColor()
	{
		return figureColor;
	}

	public void setFigureColor(Color figureColor)
	{
		this.figureColor = figureColor;
	}

	public Color getAllColor()
	{
		return allColor;
	}

	public void setAllColor(Color allColor)
	{
		this.allColor = allColor;
	}

	public Color getSelectionColor()
	{
		return selectionColor;
	}

	public void setSelectionColor(Color selectionColor)
	{
		this.selectionColor = selectionColor;
	}

	public Color getLabelColor()
	{
		return labelColor;
	}

	public void setLabelColor(Color labelColor)
	{
		this.labelColor = labelColor;
	}

}
